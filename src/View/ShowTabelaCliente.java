       public void readTable(){
       ClienteData DAO = new ClienteData();
       
       DefaultTableModel modelo  = (DefaultTableModel)jTableCliente.getModel();
       
       modelo.setNumRows(0);
       
       for(TabelaCliente p: DAO.LerTabela()){
           
           modelo.addRow(new Object[]{
               p.getID(),
               p.getNome(),
               p.getCPF(),
               p.getEndereco(),
               p.getEmail(),
               p.getTelefone()   
           });
       }} 
