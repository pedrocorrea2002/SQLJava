    private void PesquisarBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PesquisarBActionPerformed
try{
      if (IDField.getText().equals("")){
      JOptionPane.showMessageDialog(this, "Caixa vazia, não foi possível efetuar a pesquisa");
      IDField.requestFocus();
      }else{
          TabelaProduto objFun = DAO3.Pesquisar3(Integer.parseInt(IDField.getText()));
          if(objFun == null){
              JOptionPane.showMessageDialog(this, "Registro não encontrado");
              IDField.setText("");
              IDField.requestFocus();
          }else{
              IngredienteField1.setText(objFun.getIngrediente1());
              IngredienteField2.setText(objFun.getIngrediente2());
              IngredienteField3.setText(objFun.getIngrediente3());
              IngredienteField4.setText(objFun.getIngrediente4());
              DescricaoField.setText(objFun.getDescricao());
              ValorField.setText((objFun.getValor_do_Produto() + ""));
              CancelarB.setEnabled(true);
              EditarB.setEnabled(true);
              DeletarB.setEnabled(true);
              PesquisarB.setEnabled(true);
              NovoB.setEnabled(false);
              SalvarB.setEnabled(false);
              IngredienteField1.setEnabled(false);
              IngredienteField2.setEnabled(false);
              IngredienteField3.setEnabled(false);
              IngredienteField4.setEnabled(false);
              DescricaoField.setEnabled(false);
              ValorField.setEnabled(false);
              IDField.setEnabled(true);
          }
          
      }
    }catch(Exception error){
        JOptionPane.showMessageDialog(this, "Erro:" + error.getMessage());
        }
    }//GEN-LAST:event_PesquisarBActionPerformed

