    public boolean Checagem(){
        if (IngredienteField1.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Campo Ingrediente1 falta ser preenchido!");
            return false;}
        if (IngredienteField2.getText().equals("")){
            if(Check1Listener()){
            return true;    
            }
            if(!(Check1Listener())){JOptionPane.showMessageDialog(this, "Campo Ingrediente2 falta ser preenchido!");
            return false;}}
        if (IngredienteField3.getText().equals("")){
            if(Check2Listener()){
            return true;    
            }
            if(!(Check2Listener())){JOptionPane.showMessageDialog(this, "Campo Ingrediente3 falta ser preenchido!");
            return false;}}
        if (IngredienteField4.getText().equals("")){
            if(Check3Listener()){
            return true;    
            }
            if(!(Check3Listener())){JOptionPane.showMessageDialog(this, "Campo Ingrediente4 falta ser preenchido!");
            return false;}}
        if (DescricaoField.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Campo Descrição falta ser preenchido!");
            return false;}
        if (ValorField.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Campo Valor_do_Produto falta ser preenchido!");
            return false;}
        
        
        
        return true;

}
    
